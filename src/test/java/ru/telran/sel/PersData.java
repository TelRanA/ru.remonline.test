package ru.telran.sel;

public class PersData {
	public String username;
	public String login;
	public String pass;
	public String phone;
	public String mail;
	public String selectVal;
	public String percent;

	public PersData(String username, String login, String pass, String phone,
			String mail, String selectVal, String percent) {
		this.username = username;
		this.login = login;
		this.pass = pass;
		this.phone = phone;
		this.mail = mail;
		this.selectVal = selectVal;
		this.percent = percent;
	}
}